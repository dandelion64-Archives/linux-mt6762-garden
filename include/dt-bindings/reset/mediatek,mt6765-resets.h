/* FIXME: CAMSYS and IMGSYS use 2 bits for each reset
 * 0: reset fmm_ck domain
 * 1: clear faxi domain
 * CLK driver will only use the first bit
 */

/* CAMSYS: 0xc */
#define MT6765_CAMSYS_LARB6_RST		0
#define MT6765_CAMSYS_LARB3_RST		2
#define MT6765_CAMSYS_CAM_RST		4
#define MT6765_CAMSYS_SENINF_RST	8
